package b_field;

public class Field {
	/*
	 * 객체의 속성, 재산 , 메모리공간 다양한 형식으로 불림
	 * attribute , property , field
	 * class block 내부에서 어디서나 사용가능한 변수 - 전역변수
	 * java에서는 객체의 실제 메모리에 저장되는 data라고 해서 Field라고 함.
	 */
	int intField;
	double doubleField;
	boolean booleanField;
	String stringField;
	// ; 끝나는 실행문은 Field만 가능
	// System.out.println("안됨");
	
} // Field class end
