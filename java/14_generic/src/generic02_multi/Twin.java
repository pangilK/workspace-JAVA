package generic02_multi;

public class Twin<T,M> {
	
	private T type;
	private M model;
	
	// getter setter 추가 alt + s + r 
	
	public T getType() {
		return type;
	}
	public void setType(T type) {
		this.type = type;
	}
	public M getModel() {
		return model;
	}
	public void setModel(M model) {
		this.model = model;
	}

	
	
}
